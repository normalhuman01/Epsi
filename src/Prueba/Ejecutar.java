    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean exit = true;
        int r = 0, opc;
        
        ScannerEpsi t = new ScannerEpsi(jTextArea1.getText());
        ScannerEpsi q = new ScannerEpsi(jTextArea1.getText());
        ParserEpsi p = new ParserEpsi();
        
        int tam_reservada = t.getTamReservada();
        int tam_OpS = t.getTamOperadorS();
        int tam_OpC = t.getTamOperadorC();
        int tam_delimitador = t.getTamDelimitador();

        do{
            opc = t.obtenerToken();
            System.out.println(opc);
            
            if(opc == 0){
                
                jTextField1.setText("Fin del programa existosamente.");
                jTextField1.setBackground(Color.green);
                jTextField1.setForeground(Color.white);
                exit = false;
            }else if(opc == 100){
                
                jTable1.setValueAt("100", r, 0);
                jTable1.setValueAt("Id", r, 1);
                jTable1.setValueAt(t.getBuffer(), r, 2);
                r++;

            }else if(opc>=200 && opc <=200+tam_reservada){
                jTable1.setValueAt(opc, r, 0);
                jTable1.setValueAt("Palabra Reservada", r, 1);
                jTable1.setValueAt(t.getBuffer(), r, 2);
                r++;
            }else if(opc == 300){
                jTable1.setValueAt("300", r, 0);
                jTable1.setValueAt("Constante entera", r, 1);
                jTable1.setValueAt(t.getBuffer(), r, 2);
                r++;
            }else if(opc == 301){
                jTable1.setValueAt("301", r, 0);
                jTable1.setValueAt("Constante real", r, 1);
                jTable1.setValueAt(t.getBuffer(), r, 2);
                r++;
            }else if(opc>=400 && opc<= 400 + tam_OpS){
                jTable1.setValueAt(opc, r, 0);
                jTable1.setValueAt("Operador", r, 1);
                jTable1.setValueAt(t.getBuffer(), r, 2);
                r++;
            }else if(opc>=407 && opc<= 407 + tam_OpC){
                jTable1.setValueAt(opc, r, 0);
                jTable1.setValueAt("Operador", r, 1);
                jTable1.setValueAt(t.getoperadorC(), r, 2);
                r++;
            }else if(opc>=500 && opc<= 500 + tam_delimitador){
                jTable1.setValueAt(opc, r, 0);
                jTable1.setValueAt("Delimitador", r, 1);
                jTable1.setValueAt(t.getBuffer(), r, 2);
                r++;
            }else if(opc == 600){
                // Comentario y salto de linea
            }else{
                exit = Error();
            }
        }while(exit);
        p.Sintactico(q);
        
        jTextArea2.setText(p.getPila());
        jTextArea3.setText(p.getSalida());
    }//GEN-LAST:event_jButton1ActionPerformed

